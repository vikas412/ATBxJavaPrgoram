package Oct.ex_03102024;

public class Lab003 {
    public static void main(String[] args) {
        int a=3;
        int b=2;
        // This is a basic calculator, with provided 2 integers.
        System.out.println ("==========The Calculator==========");
        System.out.println ("Value of variable a is:" + a); // Printing value of a
        System.out.println ("Value of variable b is:" + b); // Printing value of b
        System.out.println ("Variable a + b =" + (a+b));  // Addition of a & b
        System.out.println ("Variable a - b =" + (a-b)); // Subtraction of a & b
        System.out.println ("Variable a * b =" + (a*b)); // Multiplication of a & b
        System.out.println ("Variable a / b =" + (a/b)); // division of a & b
        System.out.println ("Variable a % b =" + (a%b)); // Modulo of a & b
    }
}
